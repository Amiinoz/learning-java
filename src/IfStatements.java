public class IfStatements {

    public static void main(String[] args) {

        // If statements = Executes a block code if conditions are met
        int age = 18;

        if(age >= 75) {
            System.out.println("You are elder state man");
        }
        else if(age >= 18) {
            System.out.println("You are an adult");
        }
        else{
            System.out.println("You are not Adult yet!");
        }
    }
}
